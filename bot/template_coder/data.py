TRIPLE_VALUES: dict[tuple[int, int, int], str] = {
    (a_code, b_code, c_code): values[index]
    for (a_code, b_code), values in _TRIPLE_ROWS.items()
    for index, c_code in enumerate(THIRD_STEP_CODES)
}


def get_triple_value(a_code: int, b_code: int, c_code: int) -> str:
    """Return a concrete triple phrase."""
    return TRIPLE_VALUES[(a_code, b_code, c_code)]


def validate_data() -> None:
    """Validate that all coder tables are internally complete."""
    expected_template_codes = set(range(1, 11))
    if set(TEMPLATES) != expected_template_codes:
        raise ValueError("TEMPLATES must contain codes 1..10")
    if set(SINGLE_VALUES) != expected_template_codes:
        raise ValueError("SINGLE_VALUES must contain codes 1..10")

    expected_pairs = {(a_code, b_code) for a_code in range(1, 11) for b_code in range(1, 11)}
    if set(PAIR_VALUES) != expected_pairs:
        raise ValueError("PAIR_VALUES must contain all 100 (A,B) pairs")

    expected_triples = {
        (a_code, b_code, c_code)
        for a_code in range(1, 11)
        for b_code in range(1, 11)
        for c_code in THIRD_STEP_CODES
    }
    if set(TRIPLE_VALUES) != expected_triples:
        raise ValueError("TRIPLE_VALUES must contain all 500 (A,B,C) triples")


validate_data()
